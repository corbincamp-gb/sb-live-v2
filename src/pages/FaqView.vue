                                        <p>SkillBridge is an opportunity for Service members to gain valuable civilian
                                            work experience through job training, employment skills training,
                                            apprenticeship, and internship opportunities during the last 180 days of
                                            military service prior to release from active duty. DOD SkillBridge connects
                                            service members with industry partners and real-world job experiences that
                                            are designed to aid the service member in finding meaningful and gainful
                                            employment after discharge. The duration of a DOD SkillBridge program must
                                            not be more than 180 days and must occur during a participating service
                                            member's last 180 days on active duty in accordance with guidance described
                                            in DoDI 1322.29.</p>
